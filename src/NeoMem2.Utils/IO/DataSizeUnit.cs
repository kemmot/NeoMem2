namespace NeoMem2.Utils.IO
{
    /// <summary>
    /// The supported units of data size.
    /// </summary>
    public enum DataSizeUnit
    {
        /// <summary>
        /// A data size represented in bytes.
        /// </summary>
        Bytes,

        /// <summary>
        /// A data size represented in kilobytes.
        /// </summary>
        Kilobytes,

        /// <summary>
        /// A data size represented in megabytes.
        /// </summary>
        Megabytes,

        /// <summary>
        /// A data size represented in gigabytes.
        /// </summary>
        Gigabytes,

        /// <summary>
        /// A data size represented in terabytes.
        /// </summary>
        Terabytes
    }
}
