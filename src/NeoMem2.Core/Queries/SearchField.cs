namespace NeoMem2.Core.Queries
{
    /// <summary>
    /// Common search fields and field collections.
    /// </summary>
    public class SearchField
    {
        /// <summary>
        /// All fields will be searched.
        /// </summary>
        public const string All = "[All]";

        /// <summary>
        /// The class field will be searched.
        /// </summary>
        public const string Class = "Class";

        /// <summary>
        /// The name field will be searched.
        /// </summary>
        public const string Name = "Name";

        /// <summary>
        /// The tags field will be searched.
        /// </summary>
        public const string Tags = "Tags";

        /// <summary>
        /// The text field will be searched.
        /// </summary>
        public const string Text = "Text";
    }
}
